package ex02_list;

public class Person {
	private String name;
	private int age;
	private String tel;

	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public String getTel() {
		return tel;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public void setTel(String tel) {
		this.tel = tel;
	}
}
